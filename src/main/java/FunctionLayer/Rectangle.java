/**
 *
 * @author ibenk
 */
public class Rectangle {
    
    public int x;
    public int y;
    public int length;
    public int width;
    public String color;

    public Rectangle(int x, int y, int length, int width, String color) {
        this.x = x;
        this.y = y;
        this.length = length;
        this.width = width;
        this.color = color;
    }
}
